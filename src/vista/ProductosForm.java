    //metodos
    
    void agregar(){
        String id=txtIdPro.getText();
        String nomprod=txtNomPro.getText();
        String preciopro=txtPrecio.getText(); 
        String stockpro=txtStock.getText(); 
        String espro=cboEstadoPro.getSelectedItem().toString();
        
        Object[] ob=new Object[5];
        ob[0]=id;
        ob[1]=nomprod;
        ob[2]=preciopro;
        ob[3]=stockpro;
        ob[4]=espro;
        
        dao.add(ob);      
        
        
    }
    
    void actualizar(){
        String id=txtIdPro.getText();
        String nomprod=txtNomPro.getText();
        String preciopro=txtPrecio.getText(); 
        String stockpro=txtStock.getText(); 
        String espro=cboEstadoPro.getSelectedItem().toString();
        
        Object[] obj=new Object[5];
        obj[0]=nomprod;
        obj[1]=preciopro;
        obj[2]=stockpro;
        obj[3]=espro;
        obj[4]=id;

        dao.actualizar(obj);
        
        
    }
    
    void eliminar(){
        int fila=tablaPro.getSelectedRow();
        if (fila==-1) {
            JOptionPane.showMessageDialog(this,"Debe seleccionar una fila");
          }else{
        dao.eliminar(id);
        
    }
    }
    
    void nuevo(){
        txtIdPro.setText("");
        txtNomPro.setText("");
        txtPrecio.setText("");
        txtStock.setText("");
        cboEstadoPro.setSelectedItem(0);
        txtIdPro.requestFocus();
        
    }    
    
    void limpiartablaPro(){
    
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i=i-1;
            
        }
    }
    
    
    
    
    
    
    
    
    
