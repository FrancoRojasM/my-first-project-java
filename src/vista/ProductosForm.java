    private void tablaProMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaProMouseClicked
        // TODO add your handling code here:
        int fila=tablaPro.getSelectedRow();
        if (fila==-1) {
            JOptionPane.showMessageDialog(this,"Debe seleccionar una fila");
        }else{
            id=Integer.parseInt(tablaPro.getValueAt(fila,0).toString());
            String nomprod=tablaPro.getValueAt(fila,1).toString();
            String preciopro=tablaPro.getValueAt(fila,2).toString();
            String stockpro=tablaPro.getValueAt(fila,3).toString();
            String espro=tablaPro.getValueAt(fila,4).toString();;
            
            txtNomPro.setText(nomprod);
            txtPrecio.setText(preciopro);
            txtStock.setText(stockpro);
            cboEstadoPro.setSelectedItem(espro);
            
                }
        
        
    }//GEN-LAST:event_tablaProMouseClicked

