package modelo;

/**
 *
 * @author Usser
 */
public class Producto {
    int id;
    String nompro;
    Double precio;
    int stock;
    String estado;

    public Producto() {
    }

    public Producto(int id, String nompro, Double precio, int stock, String estado) {
        this.id = id;
        this.nompro = nompro;
        this.precio = precio;
        this.stock = stock;
        this.estado = estado;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNompro() {
        return nompro;
    }

    public void setNompro(String nompro) {
        this.nompro = nompro;
    }

    public Double getPrecio() {
        return precio;
    }

    public void setPrecio(Double precio) {
        this.precio = precio;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    
    
}
